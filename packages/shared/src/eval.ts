/**
 * V5 — Supervisor orchestration & eval harness (ADR 0032).
 *
 * A standardized eval contract: an `EvalSuite` is a named collection of
 * `EvalTask`s (a prompt + optional fixture setup + success criteria). An
 * `EvalRunner` (in `@triangle/eval`) executes a suite against a harness/model,
 * records per-task pass/fail + token/duration metrics, and indexes the outcome
 * into `ProjectMemory` (V4) so future runs can learn from past eval results.
 *
 * This module is pure types (no logic) so main, preload, renderer, and the
 * `@triangle/eval` package all agree on the shapes.
 */
import type { HarnessId } from './agent.js';
import type { SuccessCriteria } from './automation.js';
import type { SessionStatus } from './session.js';

/**
 * A standardized eval suite — a named collection of tasks run against a
 * harness/model to track agent performance over time. Built-in suites ship in
 * `templates/evals/`; user suites persist per-project under
 * `.triangle/evals/`.
 */
export interface EvalSuite {
  /** Stable id (built-ins use the eval filename stem; user ids are generated). */
  id: string;
  /** Human-readable name. */
  name: string;
  /** Short description of what the suite exercises. */
  description: string;
  /** The tasks in the suite, in execution order. */
  tasks: EvalTask[];
  /** True for the built-in suites shipped with the app. */
  builtIn?: boolean;
}

/**
 * A single eval task: a prompt handed to the agent, optional fixture setup,
 * and success criteria evaluated after the run. `maxTokens` / `timeoutMs`
 * bound the run; absent values fall back to the runner's defaults.
 */
export interface EvalTask {
  /** Stable id (unique within the suite). */
  id: string;
  /** Human-readable name. */
  name: string;
  /** The prompt handed to the agent. */
  prompt: string;
  /**
   * Optional fixture setup description — a free-text description of the
   * project state the task expects (e.g. "a project with a broken fragment
   * shader in src/shaders/foo.frag"). The host prepares the fixture before
   * running the task; absent means the active project is used as-is.
   */
  setup?: string;
  /** Success criteria evaluated after the run (reuses the V2/V3 shape). */
  successCriteria: SuccessCriteria;
  /** Optional token budget for the run. */
  maxTokens?: number;
  /** Optional timeout (ms) after which the run is cancelled. */
  timeoutMs?: number;
}

/**
 * A single eval run: one execution of a suite against a harness/model. Results
 * are indexed into `ProjectMemory` (V4) so future runs can recall past eval
 * outcomes via the dynamic-context pipeline.
 */
export interface EvalRun {
  /** Stable id (generated by the runner). */
  id: string;
  /** The suite that was run. */
  suiteId: string;
  /** The task ids included in this run (subset, when a suite is partially run). */
  taskIds: string[];
  /** The harness the suite ran against. */
  harness: HarnessId;
  /** Resolved model id, when known. */
  model?: string;
  /** Epoch ms the run started. */
  startedAt: number;
  /** Epoch ms the run ended, when terminal. */
  endedAt?: number;
  /** Terminal status (reuses the session status vocabulary). */
  status: SessionStatus;
  /** Per-task results, in task order. */
  results: EvalTaskResult[];
  /** Total tokens consumed across all tasks, when known. */
  totalTokens?: number;
  /** Total wall-clock duration (ms) across all tasks. */
  totalDurationMs?: number;
  /** Error message when the run itself errored (distinct from a task failing). */
  error?: string;
}

/**
 * The outcome of one task in an eval run. `passed` is the headline signal
 * (derived from the success criteria + the run's terminal status); the
 * remaining fields support the eval dashboard's trend charts.
 */
export interface EvalTaskResult {
  /** The task id this result is for. */
  taskId: string;
  /** The agent run id that executed the task (correlates with session history). */
  runId: string;
  /** Whether the task's success criteria were met. */
  passed: boolean;
  /** Terminal status of the underlying agent run. */
  status: SessionStatus;
  /** Tokens consumed by the run, when known. */
  tokens?: number;
  /** Wall-clock duration (ms) of the run. */
  durationMs?: number;
  /** Error message when the run errored. */
  error?: string;
  /** One-line transcript summary (e.g. "2 writes, verification passed"). */
  transcriptSummary?: string;
}

/**
 * Pushed to the renderer during an eval run so the Eval Dashboard can show
 * live progress (which task is running, its status, an optional message).
 */
export interface EvalProgressEvent {
  /** The eval run id. */
  runId: string;
  /** The task id this progress event is for. */
  taskId: string;
  /** The task's current status. */
  status: SessionStatus;
  /** Optional human-readable message (e.g. "starting", "verifying"). */
  message?: string;
}
